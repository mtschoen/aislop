import fs from "node:fs";
import path from "node:path";
import { getSourceFiles, isAutoGenerated } from "../../utils/source-files.js";
import type { Diagnostic, EngineContext } from "../types.js";
import { checkJsImport, extractJsImports } from "./hallucinated-imports-js.js";
import {
	jsDepsForFile,
	loadManifest,
	pythonDepsForFile,
	readJson,
} from "./hallucinated-imports-manifest.js";
import { collectTsPathAliases } from "./js-import-aliases.js";
import { collectWorkspaceDirs } from "./js-workspaces.js";
import { isNonProductionPath } from "./non-production-paths.js";
import { PYTHON_IMPORT_TO_PIP, PYTHON_STDLIB } from "./python-data.js";

const JS_EXTENSIONS = new Set([".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs"]);
const PY_EXTENSIONS = new Set([".py"]);

const extractPyImports = (content: string): { spec: string; line: number }[] => {
	const lines = content.split("\n");
	const results: { spec: string; line: number }[] = [];
	let inDoc: string | null = null;
	let typeCheckIndent = -1;
	for (let i = 0; i < lines.length; i++) {
		const raw = lines[i];
		const line = raw.trim();

		if (inDoc) {
			if (line.includes(inDoc)) inDoc = null;
			continue;
		}
		if (line === "" || line.startsWith("#")) continue;

		// Triple-quoted strings (docstrings, usage examples) hold import-shaped prose, not imports.
		const triples = line.match(/"""|'''/g);
		if (triples) {
			if (triples.length % 2 === 1) inDoc = triples[triples.length - 1];
			continue;
		}

		const indent = raw.length - raw.trimStart().length;
		if (typeCheckIndent >= 0 && indent <= typeCheckIndent) typeCheckIndent = -1;
		// Imports under `if TYPE_CHECKING:` are type-only — often typeshed-internal or dev-only deps.
		if (/^if\s+(?:[\w.]+\.)?TYPE_CHECKING\b/.test(line)) {
			typeCheckIndent = indent;
			continue;
		}
		if (typeCheckIndent >= 0) continue;

		// from X import ... — skip relative imports starting with .
		const fromMatch = line.match(/^from\s+([\w.]+)\s+import\b/);
		if (fromMatch && !fromMatch[1].startsWith(".")) {
			results.push({ spec: fromMatch[1], line: i + 1 });
			continue;
		}
		// import X[, Y as Z, ...]
		const importMatch = line.match(/^import\s+([\w.,\s]+?)(?:\s+as\s+\w+)?\s*$/);
		if (importMatch) {
			for (const part of importMatch[1].split(",")) {
				const cleaned = part.trim().split(/\s+as\s+/)[0];
				if (cleaned && !cleaned.startsWith(".")) {
					results.push({ spec: cleaned, line: i + 1 });
				}
			}
		}
	}
	return results;
};

const normalizePyName = (name: string): string => name.toLowerCase().replace(/_/g, "-");

const pyDepSatisfiesImportRoot = (root: string, pyDeps: Set<string>): boolean => {
	const normalized = normalizePyName(root);
	for (const dep of pyDeps) {
		if (dep === normalized || dep.startsWith(`${normalized}-`)) return true;
	}
	return false;
};

const checkPyImport = (spec: string, pyDeps: Set<string>): string | null => {
	const root = spec.split(".")[0];
	if (PYTHON_STDLIB.has(root)) return null;
	if (pyDepSatisfiesImportRoot(root, pyDeps)) return null;
	const normalized = normalizePyName(root);
	const distributions = PYTHON_IMPORT_TO_PIP[root] ?? PYTHON_IMPORT_TO_PIP[normalized];
	if (distributions?.some((dist) => pyDeps.has(normalizePyName(dist)))) return null;
	return root;
};

export const detectHallucinatedImports = async (context: EngineContext): Promise<Diagnostic[]> => {
	const rootPkg = readJson(path.join(context.rootDirectory, "package.json"));
	const workspaceDirs = collectWorkspaceDirs(context.rootDirectory, rootPkg);
	const manifest = loadManifest(context.rootDirectory);
	if (!manifest.hasJsManifest && !manifest.hasPyManifest) return [];
	const tsAliasMatchers = manifest.hasJsManifest
		? collectTsPathAliases(context.rootDirectory, workspaceDirs)
		: [];

	const diagnostics: Diagnostic[] = [];
	const files = getSourceFiles(context);

	for (const filePath of files) {
		const ext = path.extname(filePath);
		const isJs = JS_EXTENSIONS.has(ext);
		const isPy = PY_EXTENSIONS.has(ext);
		if (!isJs && !isPy) continue;
		if (isJs && !manifest.hasJsManifest) continue;
		if (isPy && !manifest.hasPyManifest) continue;
		if (isAutoGenerated(filePath)) continue;
		const pyDeps = isPy ? pythonDepsForFile(manifest, filePath, context.rootDirectory) : null;
		if (isPy && !pyDeps) continue;

		let content: string;
		try {
			content = fs.readFileSync(filePath, "utf-8");
		} catch {
			continue;
		}
		const relPath = path.relative(context.rootDirectory, filePath);
		if (isNonProductionPath(relPath)) continue;

		const jsDeps = isJs ? jsDepsForFile(manifest, filePath, context.rootDirectory) : null;
		const imports = isJs ? extractJsImports(content) : extractPyImports(content);
		for (const { spec, line } of imports) {
			const hallucinated = isJs
				? checkJsImport(
						spec,
						jsDeps ?? manifest.jsDeps,
						tsAliasMatchers,
						filePath,
						context.rootDirectory,
					)
				: checkPyImport(spec, pyDeps ?? manifest.pyDeps);
			if (!hallucinated) continue;
			const manifestLabel = isJs ? "package.json" : "requirements.txt / pyproject.toml / Pipfile";
			diagnostics.push({
				filePath: relPath,
				engine: "ai-slop",
				rule: "ai-slop/hallucinated-import",
				severity: "error",
				message: `Imports "${hallucinated}" but it's not declared in ${manifestLabel}${isPy ? " and isn't Python stdlib" : ""}`,
				help: "Most often this is an LLM hallucinating a plausible-sounding package name. Either add the package to your manifest, or correct the import.",
				line,
				column: 1,
				category: "AI Slop",
				fixable: false,
			});
		}
	}

	return diagnostics;
};
